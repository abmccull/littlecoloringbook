export function getStockCover(id: string): StockCover | undefined {
  return stockCovers.find((c) => c.id === id);
}

export function filterStockCovers(filters?: {
  ageRange?: string;
  lean?: string;
  tags?: string[];
}): StockCover[] {
  let result = stockCovers;
  if (filters?.ageRange) result = result.filter((c) => c.ageRange === filters.ageRange);
  if (filters?.lean) result = result.filter((c) => c.lean === filters.lean);
  if (filters?.tags?.length) {
    result = result.filter((c) => filters.tags!.some((t) => c.tags.includes(t)));
  }
  return result;
}
